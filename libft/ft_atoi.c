#include "libft.h"

int			ft_atoi(const char *str)
{
	long	r;
	short	sign;

	r = 0;
	sign = 1;
	if (*str == '-' || *str == '+')
		sign = 44 - *str++;
	while (*str >= '0' && *str <= '9')
		r = r * 10 + *str++ - '0';
	return (sign * (int)r);
}
